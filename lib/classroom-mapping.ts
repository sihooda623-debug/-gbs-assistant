/**
 * 수업명과 선생님 약칭으로 교실을 조회합니다
 * @param subject 수업명
 * @param teacher 선생님 약칭 (2글자)
 * @returns 교실명
 */
export function getClassroom(subject: string, teacher: string): string {
  const teacherMap = CLASSROOM_MAP[subject];
  if (teacherMap && teacher) {
    return teacherMap[teacher] || "";
  }
  return "";
}
